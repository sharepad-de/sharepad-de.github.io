<Section title="Anleitung: Ressourcen-Foto erstellen">
	<P>
		Ein Ressourcen-Foto dient zur schnellen Orientierung des Nutzers in sharePAD. Je besser die
		Ressource auf dem Foto als solche erkennbar ist, umso leichter kann der Nutzer es von anderen
		unterscheiden, vor allem weil in sharePAD das Bild sehr klein dargestellt wird. Deshalb
		empfehlen wir folgende Vorgehensweise beim Fotografieren:
	</P>

	<img
		src="/up.png"
		alt="Beispiel eines Ressourcen-Fotos: weißer VW Up, fotografiert im 45-Grad-Winkel von schräg vorne"
		class="my-8 rounded-xl shadow-lg max-w-full md:max-w-lg mx-auto"
	/>

	<UL>
		<LI>
			<strong>Abstand:</strong> Mindestens 5 Meter Abstand zum Fahrzeug halten, um den
			Fischaugeneffekt zu vermeiden (bei dem die Front des Autos unnatürlich verzerrt wirkt).
		</LI>
		<LI>
			<strong>Winkel:</strong> Das Auto etwa im 45-Grad-Winkel von schräg vorne fotografieren.
		</LI>
		<LI>
			<strong>Ausrichtung:</strong> Die Unterseite des Autos möglichst parallel zum unteren
			Bildrand ausrichten.
		</LI>
		<LI>
			<strong>Beleuchtung:</strong> Die Lichtverhältnisse sollten gut sein.
		</LI>
	</UL>

	<P>
		sharePAD stellt das Bild automatisch frei und dreht es so, dass das Fahrzeug nach rechts
		ausgerichtet ist. Darum müssen Sie sich beim Fotografieren also nicht kümmern.
	</P>
</Section>

